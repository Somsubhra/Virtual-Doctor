<h1>First-Aid: Cuts and Scrapes</h1><hr>
<p>
Follow these steps when treating cuts and scrapes at home:
<br />
1. Stop the bleeding by applying pressure using a gauze pad or clean cloth.If the bleeding persists after several minutes of applying pressure, get immediate medical attention.
<br />
2. Keep the wound clean by washing the area with mild soap and water and removing any dirt.
Dry the area gently with a clean cloth, and cover the wound with a protective bandage.
Change the bandage at least once a day.
If the wound becomes tender to the touch and red or oozes fluid, see your doctor.
<br />
3. If your cut is more serious and the bleeding does not stop on its own or the cut is large, deep, or rough on the edges, try to stop the bleeding by applying pressure directly to the injury using a sterilized gauze pad or clean cloth.
<br />
4. Maintain pressure on the wound until the bleeding stops.
Then consult your physician. A tetanus booster may be required if you haven't had one for a while. 
<br />


5. Summon emergency medical assistance immediately. 
<br />
