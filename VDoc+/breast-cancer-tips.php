<h1>Health Tips-Breast Cancer</h1><hr>
<p>
1. One of the most important things you can do for yourself while fighting breast cancer is to get plenty of rest. Do your best to get a full night's sleep. And, if you feel tired during the day, take the time to sleep or rest to allow your body time to recover.
<br />
2. Eat a health diet. The healthier your body is, the better prepared it will be to fight the breast cancer and to recover from breast cancer treatment. Be sure to include plenty of protein in your diet because the amino acids found in protein are used by your body to help repair damage.<br />
3. Sometimes, eating can be a difficult task if you are undergoing treatments for breast cancer. Surgery, chemotherapy, radiation, and medications can adversely affect your appetite and digestive system. You might experience nausea, vomiting, changes in taste and smell, difficulty swallowing and chewing, constipation, and pain. If you are experiencing these problems, you need to take steps to increase your appetite and to make eating easier. <br />
4. If you are undergoing external beam radiation, you may experience skin irritation or sensitivity. To assist with this problem, you should avoid using talcum powder, soaps, and deodorants. When you wash, be gentle and do not scrub or rub the area. Extreme hot or cold temperatures can also be problematic to your skin.

Therefore, tight or restrictive clothing can also aggravate your skin, as can nylon and synthetic materials. Wear loose clothing made of cotton instead. In addition, if you need to tape bandages to your skin, use paper tape rather than adhesive tape.

 

