<h1>Health Tips-Mouth Cancer</h1><hr />
<p>
1. Always brush and floss your teeth regularly. An unhealthy mouth reduces your immune system and inhibits your body’s ability to fight off potential cancers.<br />
2. Do not smoke (or chew) any type of tobacco product. If you are a smoker, even with a casual habit, make the decision to stop.<br />
3. Drink alcohol in moderation (one to two drinks per day) and never binge drink. The risk of developing oral cancer increases with the amount and length of time alcohol and tobacco products are used.<br />
4. Limit your exposure to the sun. We all know we need to use sunscreen, but do we remember to apply it to our lips? Always use UV-A/B-blocking sun protection on your lips when you are in the sun. Repeated exposure increases the risk of cancer on the lips, especially the lower lip. There are a lot of handy lipstick-size sunscreen tubes to pop in your purse for everyday use. Some even come with a colour tint and flavour to replace your regular lip gloss or moisturizer.<br />
5. Exercise regularly. An active lifestyle is known to boost the immune system and help ward off cancer.<br />
6. Choose cancer-fighting foods in your diet. The American Institute for Cancer Research recommends you eat lots of beans, berries, cruciferous vegetables (like cabbage and broccoli), dark green leafy vegetables, flaxseed, garlic, grapes, green tea, soy and tomatoes for their role in cancer prevention. "Right now the effect of diets and nutrition in preventing cancer is hot," says Dr. Lewei Zhang, a professor of oral and biomedical sciences at the University of British Columbia.<br />
7. How you prepare those foods is also important in the prevention of cancer. Replace frying and grilling with baking, boiling or steaming. Use healthy spices like garlic, ginger and curry powder for added flavour.<br />
8. See your dentist or dental hygienist regularly (at least every six months) and ask for an oral cancer screening to be done.<br />
9. Conduct a self exam at least once a month. Plan to do it the same time you do your breast self-exam. It only takes a few minutes of your time, but could make a big difference in your life. Buy a handy mouth mirror (available at most pharmacies) for those hard-to-see areas. Be sure to check the back and sides of your tongue. If you see or feel anything suspicious – lumps, bumps, tender areas, white, red or grey patches, see your dentist to have it checked.
 

