<h1>Health Tips-Asthma</h1><hr>
<p>
    1. The first step to treatment is really to understand what can cause the problem. The person needs to be aware what they may be allergic to because it is only then that they would be able to avoid all of these things to prevent the asthma attacks from occurring.<br />
    2. Foods that are rich in salt or sodium content should not be included in an asthmatic person’s regular diet. Instead, one should focus on the intake of foods that are rick in magnesium not only because it is healthy but it also has other healthy benefits on the person.<br />
    3. A person diagnosed with asthma should know the proper way of breathing. Sometimes, people die of asthma because when they are suffering from an asthma attack, they do not know other ways on how they could breathe. It is important for asthmatic people to practice breathing exercises every day.<br />
    4. Asthmatic people must also take extra care in the foods that they eat. It is important that they know which foods they are allergic to. Perhaps they could also incorporate more in-take of fresh fruits and vegetables to lessen the risk of suffering from frequent asthma attacks.<br />
    5. Submitting one’s self to regular exercise can benefit their health gratefully. For one, it could strengthen their muscles especially the lungs to enable a person to breath as normally as possible. Regular exercise can also open the chest to help a person breath even during an attack.<br />
