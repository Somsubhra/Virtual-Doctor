<h1>First-Aid: Mouth to Mouth Resuscitation</h1><hr />
<p>
Follow these steps when giving mouth to mouth resuscitation at home:
<br />
1. Before you can begin mouth-to-mouth resuscitation, you must be sure the person's airway is clear. If the person does not begin breathing once the airway is clear, perform mouth-to-mouth resuscitation.
<br />
2. To begin mouth-to-mouth resuscitation, position the victim so you can check for breathing by laying the person on his or her back on a flat, firm surface. Place yourself next to the person's neck and shoulders. Extend the person's neck gently, and open the mouth and airway by lifting the chin.
<br />
3. To determine whether the victim is breathing, place your ear above the person's mouth and listen for the sounds of inhaling or exhaling. Feel for air against your cheek and watch for motion in the victim's chest.
<br />
4. If the victim is not breathing, begin mouth-to-mouth resuscitation immediately. Pinch the victim's nostrils closed with your thumb and forefinger. Take a deep breath, and make a seal around the victim's mouth with your mouth. Breathe slowly into the victim's mouth twice, checking to be sure the victim's chest rises each time you breathe. After the second breath, turn your head, listen for air leaving the victim's lungs and watch to see if the chest falls.
<br />
5. Next, check to see if the victim has a pulse. Place two fingers on the victim's carotid artery, just to the side of the Adam's apple, to feel for movement. If the artery is pulsating, continue mouth-to-mouth resuscitation in the same way, blowing a deep breath into the victim every 5 seconds--12 breaths every minute. If the artery is not pulsating, begin cardiopulmonary resuscitation (CPR).
<br />
6. Continue to breathe for the person until he or she breathes on his or her own or until professional medical help arrives. 
