<p>
1. Bromelain is extremely effective for bronchial conditions as it improves lung functions, softens mucous and helps to loosen the mucous. If you are frequently exposed to environmental toxins or cigarette smoke, taking vitamin C and vitamin E can reduce the damage and protect your lungs.
<br />
2. Liquorice is anti-inflammatory, anti-viral and can be very useful with bronchial conditions. Zinc gluconate lozenges really help to reduce the coughing and sore throat. (The above supplements are a/v from FSC on 0800 083 0356 Olive Leaf Extract has powerful anti bacterial qualities which have been shown to help with respiratory conditions - take 3 capsules daily to help boost your immune system Include a good quality multi vitamin and mineral in your regimen.

<br />
3. Inhaling steam is really helpful for opening up the lungs. Add a few drops of Olbas Oil or any pure eucalyptus oil into a bowl of boiling water. Place a towel over your head and really inhale the steam through your nose. If you do this 4-5 times daily it helps to loosen up your chest. If the infection becomes serious and you are prescribed antibiotics, as soon as you finish the course begin taking the healthy bacteria acidophilus/bifidus for at least 6 weeks to replenish healthy bacteria in the gut which in turn helps immune function. <br />
4. Echinacea taken regularly helps prevent viral bronchitis from progressing into a more dangerous bacterial infection. <br />
5. The herbs mullein, wild cherry bark, liquorice and lomatium are useful expectorants and have anti viral properties. These herbs are available as tinctures and taken on a regular basis can help loosen mucous, reduce the coughing and help fight the infection. Also available from FSC as above. <br />
6. In general terms whenever you are suffering from a bronchial infection you should stay in bed and rest for at least 2 days. The more you try to struggle on – the slower your recovery. Use an Ultrabreathe – a small inexpensive device that helps to exercise the lungs. 
